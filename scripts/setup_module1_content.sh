      1) reason="new_symptoms" ;;
      2) reason="med_refill" ;;
      3) reason="lab_review" ;;
      4) reason="vaccination" ;;
      5) reason="annual_physical" ;;
    esac
    echo "V${i},${pid},${date},${site},${reason}"
  done
} > "${VIS_FILE}"

