PAT_FILE="${RAW_DIR}/patients.csv"
{
  echo "patient_id,age,sex,condition"
  for i in $(seq -w 1 "${PAT_N}"); do
    age=$(( (10#$i % 70) + 18 ))
    sex=$((10#$i % 2))
    if [[ "${sex}" -eq 0 ]]; then s="F"; else s="M"; fi
    case $((10#$i % 5)) in
      0) c="hypertension" ;;
      1) c="type2_diabetes" ;;
      2) c="asthma" ;;
      3) c="hyperlipidemia" ;;
      4) c="migraine" ;;
    esac
    echo "P${i},${age},${s},${c}"
