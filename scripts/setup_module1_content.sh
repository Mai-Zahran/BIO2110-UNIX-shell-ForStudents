min_lines_check() {
  local path="$1"
  local min="${2:-20}"
  local base
  base="$(basename "$path")"
  if [[ "$base" == "hello.sh" ]]; then
    return 0
  fi
  local n
  n="$(wc -l < "$path" | tr -d ' ')"
  if [[ "$n" -lt "$min" ]]; then
    echo "ERROR: '$path' has $n lines (< $min). Add more lines." >&2
    exit 1
  fi
}

write_file() {
  local path="$1"
  cat > "$path"
  min_lines_check "$path" 20
}

write_file_allow_short() {
  local path="$1"
  cat > "$path"
}

if [[ -d "$MOD1" && "$FORCE" -eq 1 ]]; then
  rm -rf "$MOD1"
fi

if [[ -d "$MOD1" && "$FORCE" -ne 1 ]]; then
