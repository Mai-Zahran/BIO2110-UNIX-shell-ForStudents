{
  echo "visit_id,patient_id,visit_date,site,reason"
  for i in $(seq -w 1 "${VIS_N}"); do
    pid="P$(printf "%03d" $(( (10#$i % PAT_N) + 1 )))"
    month=$(( (10#$i % 12) + 1 ))
    day=$(( (10#$i % 27) + 1 ))
    date="$(printf "2025-%02d-%02d" "${month}" "${day}")"
    case $((10#$i % 4)) in
      0) site="ED" ;;
      1) site="clinic" ;;
      2) site="telehealth" ;;
      3) site="lab" ;;
