  case "$1" in
    --base)
      BASE="${2:?Missing value for --base}"
      if [[ "${BASE}" != /* ]]; then
        BASE="${REPO_ROOT}/${BASE}"
      fi
      shift 2
      ;;
    --force)
      FORCE=1
      shift
      ;;
    -h|--help)
      sed -n '1,60p' "$0"
      exit 0
      ;;
    *)
      echo "Unknown argument: $1" >&2
